# Calcular a média de 3 notas do aluno.

print("Descubra a média de 3 notas.")

nota1 = float(input("Escreva a sua primeira nota:"))
nota2 = float(input("Escreva a sua segunda nota:"))
nota3 = float(input("Escreva a sua terceira nota:"))

print("A sua média é:", (nota1 + nota2 + nota3)/3 )
