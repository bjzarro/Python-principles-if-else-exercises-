# Avaliar a nota final do aluno em A, B, C, D, E, F.

nota = float(input("Informe sua nota anual escolar:"))

if nota >= 90:
    print("Sua nota é A.")

elif nota >= 80:
    print("Sua nota é B.")

elif nota >= 70:
    print("Sua nota é C.")

elif nota >= 60:
    print("Sua nota é D.")

else:
    print("Sua nota é F.")

